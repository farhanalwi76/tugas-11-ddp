def luas_segitiga(alas, tinggi):
    hasil= 0.5 * alas * tinggi
    print("hasil luas segitiga dari", 0.5, "", alas,"", tinggi, "=", hasil)
def keliling_segitiga(a, b, c):
    hasil= a + b + c
    print("hasil keliling segitiga dari", a, "+", b,"+", c, "=", hasil)
def luas_persegi(sisi):
    hasil= sisi * sisi
    print("hasil luas persegi dari", sisi, "*", sisi, "=", hasil)
def keliling_persegi(sisi):
    hasil= 4 * sisi
    print("hasil keliling persegi dari", 4, "*", sisi, "=", hasil)
def luas_persegi_panjang(panjang, lebar):
    hasil= panjang * lebar
    print("hasil luas persegi panjang dari", panjang, "*", lebar, "=", hasil)
def keliling_persegi_panjang(panjang, lebar):
    hasil= 2 * (panjang + lebar)
    print("hasil keliling persegi panjang dari", 2, "*", panjang,"+", lebar, "=", hasil)
